#include <bits/stdc++.h>

using namespace std;

int main () {
	map<string,char> m; 
	map<char,string> q;
	m[".-"]='A';	q['A']=".-";
	m["-..."]='B';	q['B']="-...";
	m["-.-."]='C'; 	q['C']="-.-.";
	m["-.."]='D';	q['D']="-..";
	m["."]='E';		q['E']=".";
	m["..-."]='F';	q['F']="..-.";
	m["--."]='G';	q['G']="--.";
	m["...."]='H';	q['H']="....";
	m[".."]='I';	q['I']="..";
	m[".---"]='J';	q['J']=".---";
	m["-.-"]='K';	q['K']="-.-";
	m[".-.."]='L';	q['L']=".-..";
	m["--"]='M';	q['M']="--";
	m["-."]='N';	q['N']="-.";
	m["---"]='O';	q['O']="---";
	m[".--."]='P';	q['P']=".--.";
	m["--.-"]='Q';	q['Q']="--.-";
	m[".-."]='R';	q['R']=".-.";
	m["..."]='S';	q['S']="...";
	m["-"]='T';		q['T']="-";
	m["..-"]='U';	q['U']="..-";
	m["...-"]='V';	q['V']="...-";
	m[".--"]='W';	q['W']=".--";
	m["-..-"]='X';	q['X']="-..-";
	m["-.--"]='Y';	q['Y']="-.--";
	m["--.."]='Z';	q['Z']="--..";
	m["..--"]='_';	q['_']="..--";
	m[".-.-"]=',';	q[',']=".-.-";
	m["---."]='.';	q['.']="---.";
	m["----"]='?';	q['?']="----";
	
	string s;
	while (cin >> s) {
		string x="";
		vector<int> a;
		for (int i=0;i<s.size();i++) {
			x+=q[s[i]];
			a.push_back(q[s[i]].size());
		}
		reverse(a.begin(),a.end());
		int n=0;
		for (int i=0;i<x.size();i++) {
			//cout << a[n];
			string p="";
			for (int j=i;j<i+a[n];j++) {
				p+=x[j];
			}
			i+=a[n]-1;
			n++;
			//cout << p << " ";
			cout << m[p];
		}
		cout << endl;
	}
	return 0;
}
